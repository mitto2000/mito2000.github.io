function hasWord(string, word) {
    var array = string.split(" "); 
 // iterate
for (var i = 0; i < array.length; i++) {
	// test each value
if (array[i] === word) {
	// return early if found
		return true;
	}
}
// return negative if end of loop is reached
return false;
   
}

